  $BODY$## Make Your Jewelry Last

Quality imitation jewelry is an investment in your style — and with the right care, your pieces can last for years, maintaining their shine and elegance.

### Why Proper Care Matters

Imitation jewelry is crafted from base metals with gold or silver plating, often adorned with semi-precious stones, enamel, or crystals. While durable, these materials require specific care to prevent tarnishing, discoloration, and stone loosening.

### Cleaning Your Jewelry

**Daily Care:**
- Wipe each piece with a soft, dry cloth after wearing
- Remove jewelry before washing hands, showering, or swimming
- Put on jewelry last when getting ready (after perfume and hairspray)

**Deep Cleaning (Monthly):**
1. Mix a drop of mild dish soap in lukewarm water
2. Gently wipe the jewelry with a soft-bristled toothbrush
3. Rinse quickly with clean water
4. Pat dry immediately with a soft cloth
5. Let air-dry completely before storing

**What to Avoid:**
- Never soak imitation jewelry in water
- Avoid ultrasonic cleaners
- Keep away from chlorine, bleach, and harsh chemicals
- Do not use silver polishing cloths on gold-toned pieces

### Storage Solutions

**The Golden Rules of Storage:**
- Store each piece in an individual soft pouch or zip-lock bag to prevent scratching
- Keep in a cool, dry place away from sunlight and humidity
- Store flat when possible — hanging jewelry can distort delicate chains
- Use silica gel packets in your jewelry box to absorb moisture

**Organize by Type:**
- Earrings on an earring holder
- Necklaces hung individually to prevent tangling
- Bangles and bracelets on a soft-lined tray

### Dealing with Tarnish

If your jewelry develops slight tarnish:
1. Make a paste of baking soda and water
2. Apply gently with a soft cloth
3. Rinse and dry thoroughly

For stubborn tarnish, our customer support team can advise on professional polishing options.

### When to Replace

Even with the best care, jewelry has a lifespan. Consider replacing pieces when:
- Plating has significantly worn through
- Stones are loose or missing
- Clasps no longer function properly

